/**
 * Generates blur + dimensions for compare images.
 * Run: node scripts/generate-compare-blur.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'
import sharp from 'sharp'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const configPath = join(root, 'config/compare-content.ts')
const blurOutPath = join(root, 'config/compare-blur-data.ts')
const metaOutPath = join(root, 'config/compare-image-meta.ts')

const srcRe = /(?:embeddedSrc|rawClinicSrc):\s*'([^']+)'/g

function extractPaths() {
	const text = readFileSync(configPath, 'utf8')
	const paths = new Set()
	let match
	while ((match = srcRe.exec(text)) !== null) {
		paths.add(match[1])
	}
	return [...paths].sort()
}

async function processImage(publicPath) {
	const filePath = join(root, 'public', publicPath.replace(/^\//, ''))
	const image = sharp(filePath)
	const { width, height } = await image.metadata()
	const blurBuffer = await image
		.clone()
		.resize(12, 12, { fit: 'inside' })
		.jpeg({ quality: 40 })
		.toBuffer()
	return {
		width: width ?? 0,
		height: height ?? 0,
		blurDataURL: `data:image/jpeg;base64,${blurBuffer.toString('base64')}`
	}
}

const paths = extractPaths()
const processed = await Promise.all(
	paths.map(async (path) => {
		const data = await processImage(path)
		return { path, ...data }
	})
)

const blurEntries = processed.map(
	({ path, blurDataURL }) => `\t'${path}': '${blurDataURL}'`
)
const metaEntries = processed.map(
	({ path, width, height }) => `\t'${path}': { width: ${width}, height: ${height} }`
)

writeFileSync(
	blurOutPath,
	`// Generated by scripts/generate-compare-blur.mjs — do not edit by hand.
export const compareImageBlurData: Record<string, string> = {
${blurEntries.join(',\n')}
}
`
)

writeFileSync(
	metaOutPath,
	`// Generated by scripts/generate-compare-blur.mjs — do not edit by hand.
export type CompareImageMeta = { width: number; height: number }

export const compareImageMeta: Record<string, CompareImageMeta> = {
${metaEntries.join(',\n')}
}
`
)

console.log(`Wrote ${paths.length} entries → compare-blur-data.ts & compare-image-meta.ts`)
