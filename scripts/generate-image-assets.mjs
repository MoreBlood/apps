/**
 * Build-time WebP + blur placeholders for landing screenshots and compare photos.
 * Run: node scripts/generate-image-assets.mjs
 */
import { mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { basename, dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'
import sharp from 'sharp'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const outConfigPath = join(root, 'config/image-assets.ts')

const PHONE_MAX_WIDTH = 640
const TABLET_MAX_WIDTH = 1024
const COMPARE_MAX_WIDTH = 1200
/** 2× for largest UI slot (.landing-icon up to 8rem ≈ 128px CSS). */
const ICON_MAX_WIDTH = 256

const landingContentPath = join(root, 'config/landing-content.ts')
const compareContentPath = join(root, 'config/compare-content.ts')

const screenshotRe = /(?:iphone|ipad|iphoneSecondary):\s*'([^']+)'/g
const compareRe = /(?:embeddedSrc|rawClinicSrc):\s*'([^']+)'/g

function extractPaths(filePath, re) {
	const text = readFileSync(filePath, 'utf8')
	const paths = new Set()
	for (const match of text.matchAll(re)) {
		paths.add(match[1])
	}
	return [...paths]
}

function maxWidthForPath(publicPath) {
	if (publicPath.startsWith('/icons/')) return ICON_MAX_WIDTH
	if (publicPath.startsWith('/compare/')) return COMPARE_MAX_WIDTH
	return /ipad/i.test(publicPath) ? TABLET_MAX_WIDTH : PHONE_MAX_WIDTH
}

function optimizedPublicPath(publicPath) {
	const dir = publicPath.startsWith('/screenshots/')
		? '/screenshots/opt'
		: publicPath.startsWith('/icons/')
			? '/icons/opt'
			: '/compare/opt'
	const base = basename(publicPath).replace(/\.[^.]+$/, '')
	return `${dir}/${base}.webp`
}

function collectIconPaths() {
	const iconDir = join(root, 'public/icons')
	return readdirSync(iconDir)
		.filter((name) => /\.(png|jpe?g)$/i.test(name))
		.map((name) => `/icons/${name}`)
}

async function processImage(publicPath) {
	const filePath = join(root, 'public', publicPath.replace(/^\//, ''))
	const maxWidth = maxWidthForPath(publicPath)
	const optimizedPath = optimizedPublicPath(publicPath)
	const outFile = join(root, 'public', optimizedPath.replace(/^\//, ''))

	mkdirSync(dirname(outFile), { recursive: true })

	const image = sharp(filePath)
	const metadata = await image.metadata()
	const width = metadata.width ?? 0
	const height = metadata.height ?? 0

	const isIcon = publicPath.startsWith('/icons/')
	const pipeline = image.clone().resize({ width: maxWidth, withoutEnlargement: true })

	await (isIcon
		? pipeline.webp({ quality: 92, alphaQuality: 100, effort: 4 })
		: pipeline.webp({ quality: 82, effort: 4 })
	).toFile(outFile)

	const optimizedMeta = await sharp(outFile).metadata()
	const blurBuffer = await sharp(filePath).resize(12, 12, { fit: 'inside' }).jpeg({ quality: 40 }).toBuffer()

	return {
		optimizedPath,
		width: optimizedMeta.width ?? width,
		height: optimizedMeta.height ?? height,
		blurDataURL: `data:image/jpeg;base64,${blurBuffer.toString('base64')}`
	}
}

const paths = [
	...extractPaths(landingContentPath, screenshotRe),
	...extractPaths(compareContentPath, compareRe),
	...collectIconPaths()
].sort()

const uniquePaths = [...new Set(paths)]

const processed = await Promise.all(
	uniquePaths.map(async (path) => {
		const data = await processImage(path)
		return { path, ...data }
	})
)

const entries = processed.map(
	({ path, optimizedPath, width, height, blurDataURL }) =>
		`\t'${path}': {\n\t\tsrc: '${optimizedPath}',\n\t\twidth: ${width},\n\t\theight: ${height},\n\t\tblurDataURL: '${blurDataURL}'\n\t}`
)

writeFileSync(
	outConfigPath,
	`// Generated by scripts/generate-image-assets.mjs — do not edit by hand.

export type ImageAsset = {
\t/** WebP path under /public */
\tsrc: string
\twidth: number
\theight: number
\tblurDataURL: string
}

export const imageAssets: Record<string, ImageAsset> = {
${entries.join(',\n')}
}
`
)

// Keep compare-* modules in sync for existing imports / tests
const comparePaths = processed.filter((p) => p.path.startsWith('/compare/'))
const blurEntries = comparePaths.map(({ path, blurDataURL }) => `\t'${path}': '${blurDataURL}'`)
const metaEntries = comparePaths.map(({ path, width, height }) => `\t'${path}': { width: ${width}, height: ${height} }`)

writeFileSync(
	join(root, 'config/compare-blur-data.ts'),
	`// Generated by scripts/generate-image-assets.mjs — do not edit by hand.
export const compareImageBlurData: Record<string, string> = {
${blurEntries.join(',\n')}
}
`
)

writeFileSync(
	join(root, 'config/compare-image-meta.ts'),
	`// Generated by scripts/generate-image-assets.mjs — do not edit by hand.
export type CompareImageMeta = { width: number; height: number }

export const compareImageMeta: Record<string, CompareImageMeta> = {
${metaEntries.join(',\n')}
}
`
)

console.log(`Optimized ${uniquePaths.length} images → config/image-assets.ts`)
